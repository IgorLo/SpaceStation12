function getRandomState() {
    return getRandomElement(['empty', 'wall']);
}

function getRandomElement(collection) {
    return collection[Math.floor(Math.random() * collection.length)];
}

export class Cell {
    constructor(state, x, y) {
        this.state = state;
        this.x = x;
        this.y = y;
    }
}

export function createMap(map) {
    if (map) {
        ySize = map.length;
        xSize = map[0].length;
    }
    let outer = new Array(ySize);
    for (let y = 0; y < ySize; y++) {
        let inner = new Array(xSize);
        for (let x = 0; x < xSize; x++) {
            if (map){
                inner[x] = map[y][x] ? new Cell(CellState.FILLED, x, y) : new Cell(CellState.EMPTY, x, y);
                continue;
            }
            inner[x] = new Cell(getRandomState(), x, y);
        }
        outer[y] = inner;
    }
    return outer;
}

export function makeID(length) {
    var result           = '';
    var characters       = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789';
    var charactersLength = characters.length;
    for ( var i = 0; i < length; i++ ) {
        result += characters.charAt(Math.floor(Math.random() * charactersLength));
    }
    return result;
}

function hashCode(str) { // java String#hashCode
    var hash = 0;
    for (var i = 0; i < str.length; i++) {
        hash = str.charCodeAt(i) + ((hash << 5) - hash);
    }
    return hash;
}

function intToRGB(i){
    var c = (i & 0x00FFFFFF)
        .toString(16)
        .toUpperCase();

    return "00000".substring(0, 6 - c.length) + c;
}

export function stringToRGB(str) {
    return intToRGB(hashCode(str));
}
