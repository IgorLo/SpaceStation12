function getRandomState() {
    return getRandomElement(['empty', 'wall']);
}

function getRandomElement(collection) {
    return collection[Math.floor(Math.random() * collection.length)];
}

export class Cell {
    constructor(state, x, y) {
        this.state = state;
        this.x = x;
        this.y = y;
    }
}

export function createMap() {
    if (savedMap) {
        ySize = savedMap.length;
        xSize = savedMap[0].length;
    }
    let outer = new Array(ySize);
    for (let y = 0; y < ySize; y++) {
        let inner = new Array(xSize);
        for (let x = 0; x < xSize; x++) {
            if (savedMap){
                inner[x] = savedMap[y][x] ? new Cell(CellState.FILLED, x, y) : new Cell(CellState.EMPTY, x, y);
                continue;
            }
            inner[x] = new Cell(getRandomState(), x, y);
        }
        outer[y] = inner;
    }
    return outer;
}
